    private void hienThiSinhVienlenForm(int index) {
        int regID = (Integer) tblStudent.getValueAt(index, 0);
        String standard = (String) tblStudent.getValueAt(index, 2);
        if (index == -1) {
            return;
        }
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection con = DriverManager.getConnection(url);

            String sql1 = "select * from students where RegID = ? and standard = ?";
            PreparedStatement statement1 = con.prepareStatement(sql1);
            statement1.setInt(1, regID);
            statement1.setString(2, standard);
            ResultSet rsStudent = statement1.executeQuery();

            while (rsStudent.next()) {
                txt_Name.setText(String.valueOf(rsStudent.getString(2)));
                txt_Address.setText(rsStudent.getString(3));
                txt_PName.setText(rsStudent.getString(4));
                txt_Con.setText(rsStudent.getString(5));
                cbb_Stan.setSelectedItem(rsStudent.getString(6));
            }

            String sql2 = "select * from standars where standardID = ?";
            PreparedStatement statement2 = con.prepareStatement(sql2);
            statement2.setString(1, standard);
            ResultSet rsStandard = statement2.executeQuery();

            while (rsStandard.next()) {
                cbb_Fees.setSelectedItem(rsStandard.getFloat("fees"));
            }
        } catch (Exception e) {
            System.out.println("Loi: " + e);
        }

    }
