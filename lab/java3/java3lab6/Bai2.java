    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int index = tblStudent.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng trước khi sửa!", "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            int n;
            //update
            //set cac tham so
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection con = DriverManager.getConnection(url);
            pstUpdate = con.prepareStatement(sqlUpdate);
            int i = Integer.parseInt(tblStudent.getValueAt(index, 0).toString());
            pstUpdate.setString(1, this.txt_Name.getText().trim());
            pstUpdate.setString(2, this.txt_Address.getText().trim());
            pstUpdate.setString(3, this.txt_PName.getText().trim());
            pstUpdate.setString(4, this.txt_Con.getText().trim());
            pstUpdate.setString(5, (String) cbb_Stan.getSelectedItem());
            pstUpdate.setInt(6, i);
            n = pstUpdate.executeUpdate();//thuc thi
            if (n > 0) {
                this.loadData();
            }
            pstUpdate.close();
            con.close();
        } catch (Exception e) {
            System.out.println("" + e);
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

