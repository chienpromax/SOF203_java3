    private void loadData() {
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection con = DriverManager.getConnection(url);
            String sql = "select * from students";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            model.setRowCount(0);
            list.clear();
            while (rs.next()) {
                model.addRow(new Object[]{rs.getInt(1),
                    rs.getString(2),
                    rs.getString(6)});
                Student sv = new Student(rs.getInt(1),
                        rs.getString(2),
                        rs.getString(3),
                        rs.getString(4),
                        rs.getString(5),
                        rs.getString(6),
                        rs.getDate(7));
                list.add(sv);
            }
            tblStudent.setModel(model);
            System.out.println("" + list.size());
            // đóng các đối tượng để đảm bảo tài nguyên
            rs.close();
            st.close();
            con.close();
        } catch (Exception e) {
            System.out.println("Loi: " + e);
        }
    }

    boolean validates() {
        if (txt_Name.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Name ko dc trong ", "Chu y", 1);
            txt_Name.requestFocus();
            return false;
        }
        if (txt_Address.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Address khong duoc de trong ", "Chu y", 1);
            txt_Address.requestFocus();
            return false;
        }
        if (txt_PName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "ParentsName khong duoc de trong ", "Chu y", 1);
            txt_PName.requestFocus();
            return false;
        }

        if (txt_Con.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Contact(Phone) khong duoc de trong va phai la 7-12 so ", "Chu y", 1);
            txt_Con.requestFocus();
            return false;
        }
        if ((cbb_Stan.getSelectedIndex() == 0)) {
            JOptionPane.showMessageDialog(this, cbb_Stan.getSelectedItem());
        }
        return true;
    }

