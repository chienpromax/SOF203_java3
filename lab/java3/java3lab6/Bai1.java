    public void loadDatabase() {
        Connection connection = null;
        Statement statement = null;
        ResultSet rs = null;
        try {
            tblModel.setRowCount(0);
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;databaseName=library;user=sa;password=123";
            connection = DriverManager.getConnection(url);
            String sql = "Select * from Books";
            statement = connection.createStatement();
            rs = statement.executeQuery(sql);

            // Nếu sách không tồn tại
            if (rs.isBeforeFirst() == false) {
                JOptionPane.showMessageDialog(this, "The book is not available!");
                return;
            }

            while (rs.next()) {
                Vector row = new Vector();
                row.add(rs.getInt(1));
                row.add(rs.getString(2));
                row.add(rs.getFloat(3));
                tblModel.addRow(row);
            }
            tblBook.setModel(tblModel); // thêm dữ liệu vào table
        } catch (Exception e) {
            System.out.println("Loi: " + e);
        }
    }

