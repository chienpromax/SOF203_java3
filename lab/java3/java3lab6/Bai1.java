    public void xoaDuLieu(int i) {
        Connection connection = null;
        PreparedStatement statement = null;
        int ret = JOptionPane.showConfirmDialog(this, "Do you want to delete?", "Message", JOptionPane.YES_NO_OPTION);
        if (ret != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;databaseName=library;user=sa;password=123";
            connection = DriverManager.getConnection(url);
            String sql = "Delete from Books where id = ? ";
            statement = connection.prepareStatement(sql);
            statement.setInt(1, i);
            statement.executeUpdate();
            loadDatabase();

            connection.close();
        } catch (Exception e) {
            System.out.println("Loi: " + e);
        } finally {
            try {
                if (statement != null) {
                    statement.close();
                }
                if (connection != null) {
                    connection.close();
                }
            } catch (Exception e) {
                System.out.println("Loi: " + e);
            }
        }
    }

