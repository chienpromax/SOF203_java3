    private void btnSqrtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSqrtActionPerformed
        // TODO add your handling code here:
        if (txtResult.getText().equals("")) {
            return;
        }
        String gt = evt.getActionCommand();
        try {
            double value = Double.parseDouble(txtResult.getText());

            if (gt.equals("SQRT")) {
                txtResult.setText(String.valueOf(Math.sqrt(value)));
            } else if (gt.equals("%")) {
                txtResult.setText("" + (value = value * 100));
            } else if (gt.equals("1/x")) {
                txtResult.setText("" + (value = 1 / value));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btnSqrtActionPerformed

