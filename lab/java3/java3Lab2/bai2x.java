    private void btnBangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBangActionPerformed
        // TODO add your handling code here:
        String input = txtResult.getText();
         try {
            int number = Integer.parseInt(input);
        if (txtResult.getText().equals("") || operator.equals("")) {
            return;
        }
        try {
            double oprand1 = Double.parseDouble(operand1);
            double oprand2 = Double.parseDouble(txtResult.getText());
            double result = 0;
            if (operator.equals("+")) {
                result = oprand1 + oprand2;
            } else if (operator.equals("-")) {
                result = oprand1 - oprand2;
            } else if (operator.equals("*")) {
                result = oprand1 * oprand2;
            } else if (operator.equals("/")) {
                result = oprand1 / oprand2;
            }
            txtResult.setText("" + result);
            isEqualspres = true;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "phai nhap so");
            resert();
        }
    }//GEN-LAST:event_btnBangActionPerformed

