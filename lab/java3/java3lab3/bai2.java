    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        // TODO add your handling code here:
        try {

            if (check()) {
                String str = "";
                str = "name " + txtHo.getText() + txtTen.getText()+"\n";
                str = str + "address " + txtBidanh.getText() + "\n";
                str = str + "mat khau " + txtPass.getText() + "\n";
                str = str + "nghi chu" + txtGhichu.getText() + "\n";
                if (rNam.isSelected()) {
                    str = str + "Sex : nam\n";
                }else if (rNu.isSelected()) {
                    str = str + "Sex : nu\n";
                }else {
                    str = str + "Sex : khong xac dinh\n";
                }
                str = str + "qualitycation " + cboQue.getSelectedItem()+ "\n";
                if (checkAn.isSelected()) {
                    str = str + "hobby : an choi\n";
                }
                if (checkNghi.isSelected()) {
                    str = str + "hobby : ngu nghi\n";
                }
                if (checkNhay.isSelected()) {
                    str = str + "hobby : nhay mus\n";
                }
                JOptionPane.showMessageDialog(this, str);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
                
    }//GEN-LAST:event_btnOkActionPerformed

