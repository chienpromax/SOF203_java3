    public boolean check() {
        if (txtHo.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "nhap ho ten");
            txtHo.requestFocus();
            return false;
        }
        if (txtTen.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "nhap ho ten");
            txtTen.requestFocus();
            return false;
        }
        if (txtPass.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "mat ma");
            txtPass.requestFocus();
            return false;
        }
        return true;
    }
