    String url = "jdbc:sqlserver://localhost:1433;databaseName=assjava3";

    public void login() {
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection con = DriverManager.getConnection(url, "sa", "123");
            PreparedStatement ps = con.prepareStatement("select * from Users where UserName = ? and Pass=?");
            ps.setString(1, txtNameLogin.getText());
            ps.setString(2, txtPass.getText());
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                if (rs.getString("role").equalsIgnoreCase("giangvien")) {
                    JOptionPane.showMessageDialog(this, "thanh cong");
                    QLDSV sv = new QLDSV();
                    sv.setVisible(true);
                    dispose();
                } else {
                    QLSV sv = new QLSV();
                    sv.setVisible(true);
                    dispose();
                }
            } else {
                JOptionPane.showMessageDialog(this, "sai tai khoan");
            }
        } catch (Exception e) {
        }
    }

    public boolean check() {
        if (txtNameLogin.getText().equals("") || txtPass.getText().equals("")) {
            return false;
        }
        return true;
    }

