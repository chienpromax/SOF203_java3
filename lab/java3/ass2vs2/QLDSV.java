    public void tim() {
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;databaseName=assjava3";
            Connection con = DriverManager.getConnection(url, "sa", "123");
            PreparedStatement ps;
            if (txtTimMaSV.getText().isEmpty()) {
                ps = con.prepareStatement("select Student.Masv,HoTen,TiengAnh,TinHoc,GDTC, (TinHoc+ TiengAnh+ GDTC)/3 as'DTB' from Student,Grade \n");
                model.setRowCount(0);
            } else {
                ps = con.prepareStatement("select Student.Masv,HoTen,TiengAnh,TinHoc,GDTC, (TinHoc+ TiengAnh+ GDTC)/3 as'DTB' from Student,Grade \n"
                        + "where Student.MaSV = Grade.Masv AND Student.Masv = ?");
                ps.setString(1, txtTimMaSV.getText());
            }
            ResultSet rs = ps.executeQuery();
            
            list.clear();
            while (rs.next()) {
                //tao doi tuong
                Gradex gra = new Gradex();
                //gan du lieu
                gra.setMasv(rs.getString("masv"));
                gra.setHoten(rs.getString("hoten"));
                gra.setTiengAnh(rs.getInt("TiengAnh"));
                gra.setTinHoc(rs.getInt("TinHoc"));
                gra.setGDTC(rs.getInt("GDTC"));
                //them vao list
                txtMaSV.setText(rs.getString("masv"));
                txtHoTen.setText(rs.getString("hoten"));
                txtEnglish.setText(String.valueOf(rs.getInt("TiengAnh")));
                txtTinHoc.setText(String.valueOf(rs.getInt("TinHoc")));
                TxtGDTC.setText(String.valueOf(rs.getInt("GDTC")));
                lbDTB.setText(String.valueOf(rs.getInt("DTB")));
                list.add(gra);
            }
            model.setRowCount(0);
            for (Gradex gra : list) {
                Object[] row = new Object[]{gra.getMasv(), gra.getHoten(), gra.getTiengAnh(),
                    gra.getTinHoc(), gra.getGDTC(), gra.getDTB()};
                model.addRow(row);
            }

        } catch (Exception e) {
            System.out.println("loi" + e);
        }
    }

