    public void loaddata() {
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;databaseName=assjava3";
            Connection con = DriverManager.getConnection(url, "sa", "123");
            PreparedStatement ps = con.prepareStatement("select top 3 Student.Masv,HoTen,TiengAnh,TinHoc,GDTC, (TiengAnh+TinHoc+GDTC)/3 as'DTB' from Student,Grade  \n"
                    + "where Student.MaSV= Grade.Masv order by 'DTB' desc");
            ResultSet rs = ps.executeQuery();
            model.setRowCount(0);
            list.clear();
            while (rs.next()) {
                //tao doi tuong
                Gradex gra = new Gradex();
                //gan du lieu
                gra.setMasv(rs.getString("masv"));
                gra.setHoten(rs.getString("hoten"));
                gra.setTiengAnh(rs.getInt("TiengAnh"));
                gra.setTinHoc(rs.getInt("TinHoc"));
                gra.setGDTC(rs.getInt("GDTC"));
                gra.setDTB(rs.getInt("DTB"));
                //them vao list
                list.add(gra);
            }
            DefaultTableModel model = (DefaultTableModel) tbDSV1.getModel();
            model.setRowCount(0);
            for (Gradex gra : list) {
                Object[] row = new Object[]{gra.getMasv(), gra.getHoten(), gra.getTiengAnh(),
                    gra.getTinHoc(), gra.getGDTC(), gra.getDTB()};
                model.addRow(row);
            }

        } catch (Exception e) {
            System.out.println("loi");
        }
    }

    public boolean check() {
        if (txtMaSV.getText().equals("") || txtEnglish.getText().equals("")
                || txtTinHoc.getText().equals("") || TxtGDTC.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "khong duoc bo trong");
            return false;
        } else {
            try {
               double av = Double.parseDouble(txtEnglish.getText());
            double gd = Double.parseDouble(TxtGDTC.getText());
            double th = Double.parseDouble(txtTinHoc.getText());

            if (av < 0 || av > 10 || gd < 0 || gd > 10 || th < 0 || th > 10) {
                JOptionPane.showMessageDialog(this, "Điểm không hợp lệ (từ 0 đến 10)");
                return false;
            }

            // Kiểm tra mã sinh viên không trùng
            String masv = txtMaSV.getText();
            for (Gradex gra : list) {
                if (gra.getMasv().equals(masv)) {
                    JOptionPane.showMessageDialog(this, "Mã sinh viên đã tồn tại");
                    return false;
                }
            }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Điểm phải là số");
            return false;
            }
        }
        return true;
    }
