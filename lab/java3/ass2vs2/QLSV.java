    public boolean check() {
        if (txtDiachi.getText().equals("") || txtEmail.getText().equals("") || txtHoten.getText().equals("")
                || txtMaSV.getText().equals("") || txtSDT.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "khong duoc bo trong");
            return false;
        } else {
            try {
                double SDT = Double.parseDouble(txtSDT.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Điểm phải là số");
                return false;
            }
        }
        return true;
    }
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (check()) {
            try {
                Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                Connection con = DriverManager.getConnection(url, "sa", "123");
                PreparedStatement ps = con.prepareStatement("insert into Student values(?,?,?,?,?,?,?)");
                ps.setString(1, txtMaSV.getText());
                ps.setString(2, txtHoten.getText());
                ps.setString(3, txtEmail.getText());
                ps.setString(4, txtSDT.getText());
                ps.setBoolean(5, (rNam.isSelected() == true) ? true : false);
                ps.setString(6, txtDiachi.getText());
                ps.setString(7, lbHinh.getText());

                int kq = ps.executeUpdate();
                if (kq == 1) {
                    JOptionPane.showMessageDialog(this, "thanh cong");
                    showData();
                } else {
                    JOptionPane.showMessageDialog(this, "that bai");
                }
                ps.close();
                con.close();
                this.loadData();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "trùng masv");
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

