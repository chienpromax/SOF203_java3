    public void showData() {
        txtMaSV.setText(tbSV.getValueAt(current, 0).toString());
        txtHoten.setText(tbSV.getValueAt(current, 1).toString());
        txtEmail.setText(tbSV.getValueAt(current, 2).toString());
        txtSDT.setText(tbSV.getValueAt(current, 3).toString());
        boolean gt = Boolean.parseBoolean(tbSV.getValueAt(current, 4).toString());
        if (gt == true) {
            rNam.setSelected(true);
        } else {
            rNu.setSelected(true);
        }
        txtDiachi.setText(tbSV.getValueAt(current, 5).toString());
        updateHinh(tbSV.getValueAt(current, 6).toString());
    }

    public void loadData() {
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection con = DriverManager.getConnection(url, "sa", "123");
            Statement stm = con.createStatement();
            String sql="select * from student";
            ResultSet rs = stm.executeQuery(sql);
            model.setRowCount(0);
            //khai bao vector
            list.clear();
            Vector vector = null;
            while (rs.next()) {
                //tao doi tuong
                vector = new Vector();
                //gan du lieu
                vector.add(rs.getString("masv"));
                vector.add(rs.getString("HoTen"));
                vector.add(rs.getString("Email"));
                vector.add(rs.getString("SoDT"));
                vector.add(rs.getBoolean("GioiTinh"));
                vector.add(rs.getString("DiaChi"));
                vector.add(rs.getString("Hinh"));
                //them vao list
                model.addRow(vector);
            }
            tbSV.setModel(model);
        } catch (Exception e) {
        }
    }
